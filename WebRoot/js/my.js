    function getTime(){
        var Nowtime = new Date();
        var NowHours = Nowtime.getHours();
        var NowMin   = Nowtime.getMinutes();
        var NowSec = Nowtime.getSeconds();
        var h=clTimer(NowHours);
        var m=clTimer(NowMin);
        var s=clTimer(NowSec);
        var timer =h+":"+m+":"+s;
        $("#p_time").html(timer);
    }


